package basics;

public class loopsconcept {

	public static void main(String[] args) {
		// while loop
		int i = 30;// initialization
		while (i >= 10) { // condition

			System.out.println(i);
			i--;// decrement
		}
		System.out.println("*****************************");

		// do while loop( from 10 to20)
		int j = 10;
		do {
		
			
			System.out.println(j);
			j++;

		} while (j <=20);

	}

}
